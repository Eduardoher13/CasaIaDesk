export interface SeedProduct {
  name: string;
  price: number;
  stock: number;
  image: string;
  description: string;
}

function img(keyword: string, lock: number): string {
  return `https://loremflickr.com/400/400/${keyword}?lock=${lock}`;
}

function prod(
  name: string,
  price: number,
  stock: number,
  keyword: string,
  lock: number,
  description?: string,
): SeedProduct {
  return {
    name,
    price,
    stock,
    image: img(keyword, lock),
    description: description ?? `${name}. Disponible en ferretería.`,
  };
}

function prodUrl(
  name: string,
  price: number,
  stock: number,
  image: string,
  description: string,
): SeedProduct {
  return { name, price, stock, image, description };
}

